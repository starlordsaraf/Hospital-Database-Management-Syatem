create database hospital;   -- added create database
\c hospital;
create table patient(pid varchar(10) primary key,phno varchar(10) not null, pname varchar(20) not null, dob date not null, addres varchar(30) not null,sex varchar(1) check (sex in ('m','f','o')), ptype varchar(3) not null check (ptype in ('ipd','opd')));
create table doctor(doc_id varchar(10) primary key,qual varchar(20) not null, consultn money not null, empid varchar(10) not null);
create table nurse(nurseid varchar(10) primary key,patientcount int not null, empid varchar(10) not null);
create table department(deptid varchar(10) primary key,deptname varchar(10) not null,deptloc text not null);
create table employee(empid varchar(10) primary key, ename varchar(20) not null, gender varchar(1) check(gender in ('m','f', 'o')), joindate date not null, dob date not null, phno varchar(10), salary money check(salary::numeric>50000 and emp_type='doc' and leave_date=NULL), leave_date date, emp_type varchar(3) not null check(emp_type in ('doc', 'nur')), deptid varchar(10));
alter table employee add foreign key(deptid) references department(deptid);
create table Out_Patient(OPD_ID varchar(10) primary key,D_Arrival date not null,Disease varchar(50) not null,Pid varchar(10) not null);
create table in_patient(ipd_id varchar(10) primary key,d_disease varchar(20) not null,d_arrival date not null,d_discharge date,pid varchar(10) not null,room_id varchar(10) not null);
create table relative(patient_ipd_id varchar(10),relative_name varchar(20) not null,relation varchar(20) not null,phone_number varchar(10) not null, primary key(patient_ipd_id,relative_name), foreign key(patient_ipd_id) references in_patient(ipd_id));
create table room(room_id varchar(10) primary key,room_cost money not null,room_type text);
create table nurse_assigned(nurse_id varchar(10),patient_ipd_id varchar(10) not null, primary key(nurse_id, patient_ipd_id));
create table doctor_assigned(doc_id varchar(10),patient_id varchar(10), primary key(doc_id,patient_id));
create table test(test_id varchar(10) primary key,test_name text not null,test_cost money not null);
--alter table test add primary key(pid);
--alter table test add primary key(test_id,pid);
create table medicine(med_id varchar(10) primary key,med_name text not null,med_cost money not null);
create table bill(bill_id varchar(10) primary key,other_charges money not null);
create table takes(med_id varchar(10) not null,patient_id varchar(10) not null,m_date date,qty int not null, primary key(med_id,patient_id));
create table has(test_id varchar(10), pid varchar(10), tdate date not null, primary key(test_id, pid), foreign key(pid) references patient(pid), foreign key(test_id) references test(test_id));   --added this new table
alter table takes add foreign key(med_id) references medicine(med_id);
alter table takes add foreign key(patient_id) references patient(pid);
alter table doctor add foreign key(empid) references employee(empid);
alter table nurse add foreign key(empid) references employee(empid);
alter table doctor_assigned add foreign key(doc_id) references doctor(doc_id);
alter table doctor_assigned add foreign key(patient_id) references patient(pid);
alter table nurse_assigned add foreign key(nurse_id) references nurse(nurseid);
alter table nurse_assigned add foreign key(patient_ipd_id) references in_patient(ipd_id);
alter table in_patient add foreign key(pid) references patient(pid);
alter table in_patient add foreign key(room_id) references room(room_id);
alter table out_patient add foreign key(pid) references patient(pid);
alter table bill add pid varchar(10) not null;
alter table bill add foreign key(pid) references patient(pid);
alter table bill add bill_date date not null;
-- alter table test add test_date date not null;
alter table department alter column deptname type varchar(30);
alter table employee drop constraint employee_check;
